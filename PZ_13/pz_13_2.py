# В двумерном списке найти среднее арифметическое элементов последних двух
# столбцов.
lst = [
    [18, 26, 3, 4],
    [54, 60, 7, 8],
    [9, 10, 11, 3]
]
col2 = [i[-2:] for i in lst]

hz = sum(x for i in col2 for x in i) / (len(lst) * 2)

print("Исходная матрица:")
for i in lst:
    print(i)
print(f"Среднее арифметическое последних двух столбцов: {hz}")